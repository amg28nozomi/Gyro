    void ModeTeam::ChangeMode() {
      // モードチームの削除
      _appMain.GetModeServer().PopBuck();
      // モードタイトルの登録
      _appMain.GetModeServer().AddMode("Title", std::make_shared<Mode::ModeTitle>(_appMain));
      _appMain.GetModeServer().TransionToMode("Title");
    }
